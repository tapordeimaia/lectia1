public class TipuriDate {
    //byte=numere cu doua cifre(foloseste mai putina memorie)
    public byte c=22;
    //short=numere mai mici ca int
    public short a=22222;
    public int f=22222222;
    //long=numere mai mari ca int; punem L la urma ca sa fie recunoscut de Java
    public long b=2222222222L;
    //float=numere cu zecimale
    public float d=2.2F;
    //double=numere mari cu zecimale
    public double e=2222222.2222222;
    //La && False are prioritate
    public boolean g= 3>2 && 7>9;
    //La ||(sau) True are prioritate
    public boolean h=5>6 || 8>7;
}
